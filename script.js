let shuffledQuestions, currentQuestionIndex, score;

const questionContainer = document.getElementById('questionContainer');
const questionElement = document.getElementById('question');
const answersContainer = document.getElementById('answers');
const resultContainer = document.getElementById('resultContainer');
const resultElement = document.getElementById('result');
const restartButton = document.getElementById('restart');
const skipButton = document.getElementById('skip');

function startGame() {
    shuffledQuestions = questions.sort(() => Math.random() - 0.5);
    currentQuestionIndex = 0;
    score = 0;
    questionContainer.classList.remove('hidden');
    resultContainer.classList.add('hidden');
    showQuestion(currentQuestionIndex);
}

function showQuestion(questionIndex) {
    const questionData = shuffledQuestions[questionIndex];
    questionElement.textContent = questionData.question;
    answersContainer.querySelectorAll('.answer').forEach(button => {
        const answerKey = button.getAttribute('data-answer');
        button.textContent = questionData.answers[answerKey];
        button.onclick = () => selectAnswer(answerKey, button);
    });
}

function selectAnswer(answerKey, button) {
    const correctAnswer = shuffledQuestions[currentQuestionIndex].correct;
    if (answerKey === correctAnswer) {
        score++;
        button.classList.add('correct');
    } else {
        button.classList.add('wrong');
    }
    setTimeout(() => {
        button.classList.remove('correct', 'wrong');
        nextQuestion();
    }, 1000);
}

function nextQuestion() {
    currentQuestionIndex++;
    if (currentQuestionIndex < shuffledQuestions.length) {
        showQuestion(currentQuestionIndex);
    } else {
        showResult();
    }
}

function showResult() {
    questionContainer.classList.add('hidden');
    resultContainer.classList.remove('hidden');
    resultElement.textContent = `لقد أجبت بشكل صحيح على ${score} من ${shuffledQuestions.length} أسئلة.`;
}

function restartGame() {
    startGame();
}

function skipQuestion() {
    nextQuestion();
}

restartButton.addEventListener('click', restartGame);
skipButton.addEventListener('click', skipQuestion);

startGame();
